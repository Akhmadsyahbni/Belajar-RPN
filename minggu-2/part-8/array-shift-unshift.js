/* Method unshift() digunakan untuk menambah element baru di awal array, sedangkan method
shift() untuk mengambil 1 element dari awal array */

let namaHewan = ["Gajah","Jerapah","Singa","Macan"];
namaHewan.unshift("Ayam"); // berfungsi menambahkan data pada urutan pertama, bisa dibilang nambahin sesuatu dipaling kiri 
console.log(namaHewan);

let namaSembako = ["Gula","Beras","Telor","Mie"];
namaSembako.shift (); // berfungsi mengahpus data pada urutan pertama, bisa dibilang nambahin sesuatu dipaling kiri 
console.log(namaSembako);
